package models.category;

public class Category {
    // Private Properties
    private Integer id;
    private String name;

    // Get/Set Methods
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
